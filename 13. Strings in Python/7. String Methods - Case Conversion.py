"""
CASE CONVERSION
upper(): Converts all characters in the string to uppercase.
lower(): Converts all characters in the string to lowercase.
title(): Converts the first character of each word to uppercase and the remaining to lowercase.
capitalize(): Converts only the first character of the entire string to uppercase and
                the rest to lowercase.
swapcase(): Swaps the case of each character - uppercase becomes lowercase and vice versa.
"""

text = "Anirudh Khurana is a cODer JH789432.;[]'a##@@ sanjay khurana"
print(text.upper())
print(text.lower())
print(text.title())
print(text.capitalize())
print(text.swapcase())
